package operators;

/**
 * Kaliyona Course: Core Java Tutorial
 * Module: Operators
 * This is an example class to illustrate relational operators in Java
 */

public class RelationalOperatorExample {
    public static void main(String[] args) {
        int num1 = 50;
        int num2 = 30;
        System.out.println("num1 is equal to num2: " + (num1 == num2));
        System.out.println("num1 is not equal to num2: " + (num1 != num2));
        System.out.println("num1 is greater than num2: " + (num1 > num2));
        System.out.println("num1 is lesser than num2: " + (num1 < num2));
    }
}
