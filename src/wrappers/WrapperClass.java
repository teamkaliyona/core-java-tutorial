package wrappers;

/**
 * Kaliyona Course: Core Java Tutorial
 * Module: Data Types
 * This is an example class to illustrate Wrappers in Java
 */

public class WrapperClass {
    public static void main(String[] args) {
        Integer number = 50;
        Long longNumber = 6789L;
        Boolean flag = true;

        System.out.println("Integer number is " + number);
        System.out.println("Long value is " + longNumber);
        System.out.println("Flag value is " + flag);
    }
}
