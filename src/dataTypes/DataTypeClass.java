package dataTypes;

/**
 * Kaliyona Course: Core Java Tutorial
 * Module: Data Types
 * This is an example class to illustrate data types in Java
 */

public class DataTypeClass {
    public static void main(String[] args) {
        byte byteNumber = 10;
        short shortNumber = 150;
        int intNumber = 2500;
        long longNumber = 456789L;

        System.out.println("Byte value is " + byteNumber);
        System.out.println("Short Value is " + shortNumber);
        System.out.println("int value is " + intNumber);
        System.out.println("long value is " + longNumber);

        float floatNumber = 56.98f;
        double doubleNumber = 7680832.683;

        System.out.println("Float value is " + floatNumber);
        System.out.println("Double value is " + doubleNumber);

        boolean flag = true;
        System.out.println("Boolean flag value is " + flag);

        char letter = 'K';
        System.out.println("Character value is " + letter);

    }
}
